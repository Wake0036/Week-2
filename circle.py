radius = float(input("What is the radius of the circle? ")) # Ask the user the radius and convert it to int
diameter = radius * 2 # determinate the diameter
circumference = 2 * 3.14 * radius # determinate the circumference
area = 3.14 * radius ** 2 # determinate the area 
print("A circle with a radius of "+str(radius)+" units will have a diameter of "+str(diameter)+" units, a circumference of "
      +str(circumference)+" units and an area of "+str(area)+" square units.") # end